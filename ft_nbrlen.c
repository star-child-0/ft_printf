#include "ft_printf.h"

int	ft_nbrlen(long int a)
{
	int	i;

	i = 1;
	if (a == -2147483648)
		return (11);
	if (a < 0)
	{
		a *= -1;
		i++;
	}
	while (a > 9)
	{
		a /= 10;
		i++;
	}
	return (i);
}
